a = float(input("Бірінші caн: "))
b = float(input("Eкінші caн: "))

operation = input("Операция (+, -, *, /): ")

if operation == "+":
    print(a + b)

elif operation == "-":
    print(a - b)

elif operation == "*":
    print(a * b)

elif operation == "/":
    print(a /b)

else:
    print("Қате операция")